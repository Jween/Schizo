package io.jween.schizo.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Indicates that a method is intended to serve other clients with given name.
 * And the very given name will be the final api name the clients see.
 * In another word, clients won't know the actual original method name.
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.METHOD)
public @interface Api {
    String value() default "";
}
